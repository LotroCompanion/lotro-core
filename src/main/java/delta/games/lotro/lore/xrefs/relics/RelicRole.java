package delta.games.lotro.lore.xrefs.relics;

/**
 * Relic roles.
 * @author DAM
 */
public enum RelicRole
{
  /**
   * Relic is a reward of quest.
   */
  QUEST_REWARD,
  /**
   * Relic is a reward of deed.
   */
  DEED_REWARD,
  /**
   * Relic may come from container.
   */
  CONTAINED_IN,
  /**
   * Relic found as ingredient in a melding recipe.
   */
  RECIPE_INGREDIENT,
  /**
   * Relic found as result in a melding recipe.
   */
  RECIPE_RESULT,
}
