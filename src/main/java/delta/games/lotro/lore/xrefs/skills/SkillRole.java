/**
 * Skill roles.
 * @author DAM
 */
public enum SkillRole
{
  /**
   * Skill found in a character class.
   */
  CLASS_SKILL,
  /**
   * Skill provided by an item.
   */
  GRANTED_BY_ITEM,
  /**
   * Skill provided by a trait.
   */
  GRANTED_BY_TRAIT
}
